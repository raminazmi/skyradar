export function getHeatmapColor(value: number, type: ForecastGridType): string | null {
    // قيم الرياح/الهبّات تصل من الخادم بوحدة km/h أصلاً (wind_speed_unit=kmh)،
    // ومفتاح ألوان Zoom Earth معرّف بـ km/h — فلا حاجة لأي تحويل.
    switch (type) {
        case 'wind':
            return toRgba(interpolateStops(value, WIND_HEATMAP_STOPS));
        case 'wind-gusts':
            return toRgba(interpolateStops(value, WIND_GUST_HEATMAP_STOPS));
        case 'temperature':
            return toRgba(interpolateStops(value, TEMP_HEATMAP_STOPS));
        case 'feels-like':
            return toRgba(interpolateStops(value, FEELS_LIKE_STOPS));
        case 'wet-bulb':
            return toRgba(interpolateStops(value, WETBULB_HEATMAP_STOPS));
        case 'precipitation':
            return value < 0.1 ? null : toRgba(interpolateStops(value, PRECIP_HEATMAP_STOPS));
        case 'pressure':
            return toRgba(interpolateStops(value, PRESSURE_HEATMAP_STOPS));
        case 'humidity':
            return toRgba(interpolateStops(value, HUMIDITY_HEATMAP_STOPS));
        case 'dewpoint':
            return toRgba(interpolateStops(value, DEWPOINT_HEATMAP_STOPS));
        case 'clouds':
            return value < 5 ? null : toRgba(interpolateStops(value, CLOUD_HEATMAP_STOPS));
        default:
            return null;
    }
}

// ─────────────────────────────────────────────────────────────────────────────
// PARTICLE COLORS — Zoom Earth style: أبيض ساطع شبه شفاف يتغير بالسرعة
//
// في Zoom Earth، الجسيمات دائماً أبيض/تركواز فاتح جداً بغض النظر عن الطبقة.
// الشفافية تزداد بزيادة سرعة الرياح لتبرز التيارات النفاثة والعواصف.
// ─────────────────────────────────────────────────────────────────────────────

/**
 * لون جسيمة الرياح — أبيض ساطع مع شفافية تتناسب مع السرعة
 * مُستخدَم لجميع الطبقات (Zoom Earth style)
 */
export function getWindParticleColor(speed: number, baseAlpha: number): string {
    // سرعة الرياح تأتي عادة بالـ m/s، نحولها لـ km/h لضبط الشفافية بدقة
    const speedKmh = speed * 3.6;
    
    // الشفافية تزداد تدريجياً مع السرعة (تيارات قوية = جسيمات أوضح)
    const speedFactor = Math.min(1.0, speedKmh / 120);
    const alpha = Math.min(0.98, baseAlpha * 0.40 + speedFactor * 0.60);

    // أبيض مع لمسة تركواز خفيفة جداً للسرعات العالية (Jet Stream effect)
    if (speedKmh > 90) {
        return `rgba(232, 248, 255, ${alpha.toFixed(3)})`;
    }
    return `rgba(255, 255, 255, ${alpha.toFixed(3)})`;
}

/**
 * لون RGB الخام لجسيمة الرياح (للحالات التي تحتاج RGB منفصل)
 */
export function getWindParticleRgb(speed: number): [number, number, number] {
    return speed * 3.6 > 90 ? [232, 248, 255] : [255, 255, 255];
}

/**
 * لون جسيمة طبقة الهطول (تُستخدَم في wind-advection mode)
 * نفس اللون الأبيض / تركواز فاتح مثل بقية الطبقات
 */
export function getPrecipParticleColor(intensity: number, baseAlpha: number): string {
    // للهطول: نفس الجسيمات البيضاء، لكن مع تمييز بسيط بالشفافية
    const speedFactor = Math.min(1.0, intensity / 15);
    const alpha = Math.min(0.90, baseAlpha * 0.60 + speedFactor * 0.35);
    return `rgba(220, 245, 255, ${alpha.toFixed(3)})`;
}

/**
 * لون جسيمة الطبقات الـscalar (temperature, pressure, humidity, etc.)
 * مُوحَّد: أبيض ساطع مثل Zoom Earth — لا اختلاف بين الطبقات
 */
export function getScalarParticleColor(_type: ForecastGridType, baseAlpha: number): string {
    // توحيد تام مع Zoom Earth: أبيض ساطع دائماً
    const alpha = Math.min(0.88, baseAlpha * 0.80);
    return `rgba(255, 255, 255, ${alpha.toFixed(3)})`;
}
