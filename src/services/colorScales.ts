type RgbaTuple = [number, number, number, number];
type ColorStop = [number, RgbaTuple];

function clamp01(value: number): number {
    return Math.max(0, Math.min(1, value));
}

function lerpColor(a: RgbaTuple, b: RgbaTuple, t: number): RgbaTuple {
    const x = clamp01(t);
    return [
        Math.round(a[0] + (b[0] - a[0]) * x),
        Math.round(a[1] + (b[1] - a[1]) * x),
        Math.round(a[2] + (b[2] - a[2]) * x),
        a[3] + (b[3] - a[3]) * x,
    ];
}

function interpolateStops(value: number, stops: ColorStop[]): RgbaTuple {
    if (stops.length === 0) return [255, 255, 255, 0];
    if (value <= stops[0][0]) return stops[0][1];

    for (let index = 0; index < stops.length - 1; index += 1) {
        const current = stops[index];
        const next = stops[index + 1];
        if (value <= next[0]) {
            const range = next[0] - current[0] || 1;
            return lerpColor(current[1], next[1], (value - current[0]) / range);
        }
    }

    return stops[stops.length - 1][1];
}

function toRgba(color: RgbaTuple): string {
    return `rgba(${color[0]},${color[1]},${color[2]},${color[3].toFixed(3)})`;
}

